SUBROUTINE DTRACK(N, SV, GX, GS, SWP, NTR)
  USE, INTRINSIC :: ISO_FORTRAN_ENV, ONLY: ERROR_UNIT, REAL64
  IMPLICIT NONE
  INTEGER, PARAMETER :: K = REAL64
  INTEGER, INTENT(IN) :: N, GS, SWP, NTR
  REAL(KIND=K), INTENT(IN) :: SV(N), GX
  INTEGER :: J
  WRITE (ERROR_UNIT,*) 'sweep', SWP, '#transfs.', NTR
  WRITE (ERROR_UNIT,*) 'G*2**', -GS, 'max|G(I,J)|', GX
  WRITE (ERROR_UNIT,*) '||G(:,J)||_F'
  DO J = 1, N
     WRITE (ERROR_UNIT,9) J, SV(J)
  END DO
9 FORMAT(I3,ES25.17E3)
END SUBROUTINE DTRACK
